        оборудования (в том числе арматуры) и к процедурам контроля и приёмки. Одна из самых серьёзных систем контроля качества — у Транснефти. Причём при закупках наиболее ответственного оборудования (например, арматуры DN300 и выше) представители
        Транснефти непрерывно контролируют весь процесс производства непосредственно на предприятии. Для этого в структуре корпорации действует ООО «Транснефть Надзор».
    </p>
