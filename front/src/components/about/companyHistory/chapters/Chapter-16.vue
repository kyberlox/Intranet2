    <p>
        Обеспечение качества промышленной продукции в СССР базировалось на системе ГОСТов, имевших силу закона. На каждом советском предприятии имелся Отдел Технического Контроля, формально имевший право не допустить отгрузки продукции, не
        соответствующей стандартам. Но на практике ОТК обычно был зависим от администрации завода, перед которой стояла задача выполнить план, и лишние препоны ей были ни к чему. Поэтому качество того, что выпускали советские заводы, нередко
        оставляло желать лучшего. Лишь военная приёмка, в рамках которой был создан институт военных представителей, независимых от производителя, действовала достаточно эффективно.
    </p>
    <p>
        После распада СССР сфера управления качеством, как и другие сферы экономики, пережила период развала. ГОСТы перестали работать, так как государство уже не могло налагать ответственность за их нарушение. Многие производители ради снижения цены
        отказались от стандартных требований к качеству, на рынках появилось огромное количество контрафакта. Международный стандарт ISO 9001, перенесенный на российскую почву, работает плохо, так как формальное внедрение системы менеджмента качества
        в наших условиях недостаточно, и наличие сертификата соответствия данному стандарту на практике ничего не гарантирует.
