            рассчитали, и уже смогли с коммерсантами обоснованно разговаривать: «Ребята, вы когда заказы берете, устанавливайте такие сроки, чтобы мы успели изготовить! Объясните заказчику, что нельзя за месяц сделать то, на что нужно два!» Вот когда
            они поняли, что мы можем, а что не можем, и начали это учитывать — стало возможным более или менее планировать производство.
        </p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ НИНА МИХАЙЛОВА:</h5>
    <blockquote>
        <p>
            Маршрутизация позволила нам по-новому подступиться к извечной проблеме кадров, к эффективному управлению персоналом. Раньше в цехе оно было на уровне: «Вить, Вась, пойди сюда, сделай то». Структуры управления не было, если кто-то где-то
            схалтурил — не поймёшь, где и кто. Без маршрутных листов мы не могли получить объективных данных для персональной оценки работников. А ведь всегда есть те, кто работает с горящими глазами, и те, кто кое-как.
        </p>
        <p>
            Теперь мы стали получать конкретную информацию о работе цеха и вскоре пришли к тому, что нужно её оцифровывать, что нам требуются не только отметки в маршрутном листе о прохождении деталью определённых этапов обработки, но и хоть какие-то
            нормы времени. Начали собирать информацию с рабочих мест, и сразу многое стало видно. Выяснялось, например, что у двух людей на одном и том же месте разная выработка. Мы искали, откуда разница, подключали технологов, чтобы решить, какие
            нормы выработки более обоснованны. С тем работником, у которого выработка ниже, начинали разбираться, почему у него работа не клеится, давали корректировки.
        </p>
        <p>
            И не обязательно сразу увольнять. Если он классный специалист, но не так настроен — надо ему поручить работу под более жестким контролем. Если он готов к развитию, к сближению с коллективом — мы с ним продолжим работать. Хотя были и
            непробиваемые, упорно живущие своей жизнью отдельно от общей системы — тогда мы с ними прощались, выводили из коллектива. Бывало даже, выводили хороших специалистов, если они саботажники. Шли на это, чтобы общая система складывалась. Вот
            такой процесс мы запустили. Подбирали новые кадры, какие-то перестановки делали.
        </p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ ВИКТОР СОЛОМИНОВ:</h5>
    <blockquote>
        <p>
            Поначалу в нашем цехе людей было мало, и управление было таким: один начальник, все остальные — исполнители. Мастера у нас только в 2013 г. появились, когда объёмы пошли большие и потребовался определённый порядок. Мы тогда начали активно
            набирать людей, готовить их, учить.
        </p>
        <p>И, главное, мы начали создавать коллектив.</p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ НИНА МИХАЙЛОВА:</h5>
    <blockquote>
        <p>
            Внутренняя дисциплина, то есть осознанное, а не из-под палки, выполнение всех необходимых требований — она прививается, только когда есть коллектив. А коллектива не было, каждый жил своей жизнью. Мы начали его строить, неформальные связи
            поддерживать, элементарно интересоваться людьми.
        </p>
        <p>К людям ведь нужно прислушиваться: они на местах больше видят, чем мы. Прежде чем человека озадачить, ты опиши ему механизм действий, объясни, зачем нужна его работа, а не просто «поди и принеси».</p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ ВИКТОР СОЛОМИНОВ:</h5>
    <blockquote>
        <p>
            Коллектив — это значит, по работе надо надеяться не только на себя, надо опираться на всех, зная, что тебя не обманут и не подставят. Человек должен быть уверен, что его поддержат и не подведут. А как это сделать... ну, тут психология.
            Надо с людьми общаться чаще, надо их проблемами жить. По должности и по уровню знания они должны тебя уважать, но не считать, что ты самодур или пуп земли. Тут обыкновенное человеческое общение. И как-то вот удалось, что сейчас люди ко
            мне в открытую идут с любыми своими проблемами.
        </p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ НИНА МИХАЙЛОВА:</h5>
    <blockquote>
        <p>
            Мы начали воспитывать кадры, продумывать и организовывать цепочки взаимодействия: откуда информация, кому и в каком виде должна прийти. По сути, начали описывать процессы. Раньше нельзя было исполнителям доверять, их постоянно приходилось
            «пинать», каждый вопрос контролировать, подключаться почти на каждом движении информации. Но постепенно появился опыт, что требует внимания, а что можно и нужно делегировать, возникли горизонтальные связи между службами, они научились
            решать вопросы между собой, и только если не находят консенсуса — подключают вертикаль. Уже никто не бежит к начальнику с вопросами «ой, паспорт не соответствует, как быть», «моя деталь проточена, что с ней дальше делать». Процедуры
            прописаны — все знают, что делать. Ответственность возложена на конкретных исполнителей, и они сами всё контролируют, не требуя вмешательства.
        </p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ ВИКТОР СОЛОМИНОВ:</h5>
    <blockquote>
        <p>
            Если раньше приходилось в цехе до позднего вечера оставаться и все выходные на заводе торчать, самому во всё вникать, решать проблемы, контролировать каждый шаг, то сейчас уже такой необходимости нет. Я теперь могу положиться на мастеров:
            они сами принимают многие конкретные решения, порой даже между собой договариваются о распределении текущих задач.
        </p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ ВЯЧЕСЛАВ СТЕПАНОВ:</h5>
    <blockquote>
        <p>
            Сейчас я уже уверен, что мои сотрудники всё сами сделают как надо. Мне не нужно непрерывно раздавать указания и контролировать. Наши конструкторы и технологи выросли как специалисты, рабочие получили навыки. Текучки у нас уже почти нет, а
            когда человек проработал 5–6 лет — он уже знает всё наизусть. И время на изготовление той или иной детали сокращается. Но им всё равно учиться приходится. Мы же станочный парк обновляем, применяем новые инструменты, а кое-где и новые
            технологии.
        </p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ НИНА МИХАЙЛОВА:</h5>
    <blockquote>
        <p>
            Сегодня мы имеем на заводе стабильный хорошо управляемый коллектив, грамотно прописанные основные процессы. Но процесс развития бесконечен. Решив одну проблему — видишь другую, которая до этого была непонятна. Тем более перед заводом
            постоянно ставятся новые задачи, мы новую продукцию осваиваем. И сейчас пришли к тому, что для быстрого развития номенклатуры нам требуются новые подходы к анализу экономики производства, одного только нормирования труда не хватает.
            Например, нужен учёт машиночасов, заложенных в каждую деталь, чтобы иметь возможность оценить её будущую себестоимость уже на этапе разработки конструкторской документации. Для быстрорастущего завода это очень важно.
        </p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ ЮРИЙ ЗИНОВЬЕВ:</h5>
    <blockquote>
        <p>
            Процесс обретения САЗом культуры производства был очень болезненный и длительный. На стабильную работу, практически не требующую вмешательства с моей стороны, завод перешёл только в 2015 г., когда Михайлова была назначена исполнительным
            директором, а Соломинов стал полномочным начальником производства.
        </p>
        <p>
            Я очень рад, что привел на САЗ надзор Транснефти. Он заставил завод жить по-другому, смотреть на производство по-другому, относиться к своему делу по-другому и в итоге конечный продукт получать совсем другой. Он мотивировал нас осваивать
            новые технологии — мы поняли, что на том оборудовании, которое есть, качественный продукт не произвести. Сейчас мы выпускаем продукцию для Транснефти в нормальном режиме, нормального качества, без просрочек, без штрафных санкций.
        </p>
        <p>
            Конечно, до идеала ещё очень далеко, но по сравнению с тем, что было в 2011 г. — это небо и земля! Главное, к чему мы в итоге пришли. Если я сейчас позвоню на САЗ и скажу кому-то из начальников служб: «Слушай, дорогой, тут срочность
            такая, некогда делать то-то и то-то, давай-ка по-быстренькому», то услышу в ответ: «Как это так «по-быстренькому»?! Нет! И вот это надо обязательно сделать, и вот то!» То есть, если раньше людей сложно было заставить выполнять правила —
            они не понимали, зачем это нужно, то теперь поди попробуй заставить их не выполнить! Люди за эти десять лет прониклись профессиональным отношением к делу. И «культура производства» — она именно в этом: не в каких-то формальных механизмах,
            а в отношении к своему делу.
        </p>
    </blockquote>
