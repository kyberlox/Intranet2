            Главным направлением стратегии развития ЭМК в те годы было формирование портфеля арматурных брендов, за которыми стоит уникальная номенклатура и тем самым доля рынка. Создание САЗа не являлось частью этой стратегии. Просто, сталкиваясь с
            очередной проблемой ИФАЗа, я часто думал: «Эх, ну почему же он так далеко?! Будь у меня производство здесь, под рукой — уж я бы с ним позанимался! Мы бы сделали его намного эффективнее!» И тут мне приносят бизнес-план, и я вижу в нём
            шаровые краны...
        </p>
