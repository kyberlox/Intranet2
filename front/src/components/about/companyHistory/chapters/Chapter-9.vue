            расчистили, отремонтировали и стали оснащаться: искать и скупать подержанное оборудование для производства вентилей и кранов. Сначала скупали универсалы, потом нам подвернулись новые токарные станки с ЧПУ, которые где-то простояли
            несколько лет нераспакованными. Ездили по области, доезжали и до Тулы, и до Москвы. Я лично ездил со Станиславом Анатольевичем, каждый станок покупал сам. Экономили как могли. Есть у меня дар поторговаться...
