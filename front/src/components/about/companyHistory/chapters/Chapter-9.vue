            негативный опыт с Красным Кутом, но сложились сразу несколько обстоятельств: имелся стабильный, хотя и не массовый спрос, к нам пришёл профессионал, глубоко понимающий технологию, была возможность недорого создать компактный участок,
            обладающий большой производительностью. Мы как раз решили, что пора прекращать делать краны по кооперации и налаживать их производство у себя. С приходом Курбатова появилась удобная возможность организовать в одном цеху не один участок, а
            сразу два.
        </p>
