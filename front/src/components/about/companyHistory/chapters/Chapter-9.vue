        <p><strong>РАССКАЗЫВАЕТ ВЯЧЕСЛАВ СТЕПАНОВ:</strong></p>
        <p>
            Я много чем занимался, такие времена были. Хотелось найти перспективное направление, чтобы применить свои знания... ну и заработать. А Лев Трофимович Бугаенко &gt; был в своё время главным инженером «Саратовгазаппарата». Мы с ним занялись
            проектированием и изготовлением газораспределительных щитов: ГРП, ГРПШ, ПГБ. Создали производство на заводе «Слава и Надежда-газ» у Вячеслава Николаевича Ганина. Оно нормально функционировало, но экономически нам было неинтересно работать
            с Ганиным. Мы от него ушли и начали искать, где бы приложить свои способности. Написали бизнес-план, хотели организовать собственное производство, стали искать инвестора. Нам посоветовали обратиться в ЭМК, мы пришли, показали своей
            бизнес-план. Но Кошкарев сказал: «Меня не интересуют эти шкафы. Я занимаюсь арматурой. Арматуру вы можете делать?»
        </p>
        <p>
            И мы согласились. Наверное, так сложилась судьба. Хотя были и другие варианты, но вышло так, что с Кошкаревым мы нашли общий язык. Я с арматурой был знаком, хотя её и не конструировал. Эти шкафы — они же насыщены арматурой: там стоят
            краны, регуляторы, обратные клапаны, отсечные. Чтобы сделать проект ГРПШ, нужен расчёт определённый, и для этого надо понимать, как это всё работает. Так что тема меня не пугала: «Надо будет — сделаем».
        </p>
    </blockquote>
    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ ЛЕВ АЛАЕВ:</strong></p>
        <p>
            И началось. Дали нам две комнаты в «Теплоремонте»: в одной стоял кульман (на первых порах на нём чертили), а вторая была моя. (Сам ЭМК уже перебрался на седьмой этаж<sup id="fnref:30"
                ><a class="footnote-ref" href="#fn:30" role="doc-noteref">3</a></sup
            >.) Завод наш был тогда только на бумаге: не было ни оборудования, ничего. Мы разобрали несколько кранов, посмотрели. Нарисовали свои конструкции. Кран — это же не атомная подводная лодка, в нём ничего такого нет. Степанов и Бугаенко в
            этом понимали, я вникал.
        </p>
        <p>Стали ездить по знакомым из тех, кто занимался мехобработкой — искали, где что можно сделать по кооперации. Начали заказывать детали. Кошкарев на базе УПТК-1 выделил нам, грубо говоря, два гаража...</p>
    </blockquote>
    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ ВЯЧЕСЛАВ СТЕПАНОВ:</strong></p>
        <p>
            На УПТК-1 у нас было метров пятьдесят квадратных. Там, как въезжаешь, по правой стороне есть боксы, где было что-то вроде механических мастерских — вот нам два таких бокса и выделили. Мы там фрезерный станок поставили, токарный. Я сам и
            токарил, и фрезеровал. Стенд сделали, качество проверяли... правда, вручную. Вначале всё было «на коленке».
        </p>
        <p>
            Людей было минимум, всего человек десять работало. Одного парня, например, брали водителем, он занялся ещё и сборкой — оказались золотые руки у него, в итоге он сам же собирал, сам же и развозил, до сих пор работает на заводе слесарем
            (кстати, лучшим).
        </p>
        <p>
            И вот в таком режиме мы выпустили порядка 500 кранов. И когда мы показали, что действительно можем их делать, когда пошла продажа (и, между прочим, неплохо пошла!) — тогда к нам уже полностью повернулись лицом: «Что вам надо, чтобы
            развивать производство?» То есть нас сначала, как щенят, бросили в воду: выплывете — хорошо, будем дальше работать; утонете — и ладно. Мы выплыли.
        </p>
