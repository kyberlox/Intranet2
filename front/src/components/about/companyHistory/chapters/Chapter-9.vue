        <p>
            С Сергеем Великановым я был знаком с третьего класса. Мы с ним вместе занимались настольным теннисом, даже делили 1–2 места на чемпионате города. На соревнования приходили Небывалов, Тоскарь. Я их всех знал. После школы я пошёл в военное
            училище, но надолго в армии не задержался, в 1991 г. вернулся в Саратов. Торговал всем подряд. Великанов, Небывалов, Тоскарь тоже торговали чем-то. Я с ними периодически виделся. И уже тогда знал о существовании Кошкарева. Но лично мы
            знакомы не были.
        </p>
        <p>
            А потом меня судьба с ним свела. Я тогда работал в ПРП «Саратовэнергоремонт» в отделе внешней кооперации, занимался поставкой запасных частей к насосному и энергетическому оборудованию из Украины: Бердянск, Сумы, Полтава. А поскольку
            делал взаимозачёты по Саратовской области, то знал всех нужных снабженцев-сбытовиков. «Простор» к тому времени распался, я работал с Тоскарем через фирму «Агромашснаб», принадлежавшую Великанову. Тоскарь меня и познакомил с Кошкаревым.
            Кошкарев, конечно, захотел проверить, кто я такой и что могу. Он загрузил мне целый КамАЗ задвижек, вентилей 15б3р — и я повёз всё это в Сумы частным образом (у меня там был начальник сбыта знакомый). Привёз обратно КамАЗ запчастей к
            насосному оборудованию для ТЭЦ. Всё это быстро превратили в ликвидный товар или в деньги, получили солидную прибыль... Ну и я начал работать с ЭМК.
        </p>
    </blockquote>
    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</strong></p>
        <p>
            Лев Алаев оказался очень полезным и надёжным партнёром! В первой же сделке, которую я ему доверил, он от нашего имени поменял арматуру на запчасти, а затем за эти запчасти мы получили в «Саратовэнерго» энергозачёты для нужных нам
            предприятий, продукцию которых смогли быстро и выгодно реализовать. Мы начали активно сотрудничать. Лев мог совершенно самостоятельно, никого вообще больше не привлекая, от нашего имени проводить длинные бартерные цепочки, в начале
            которых была арматура, а в конце — деньги, причём с очень хорошей прибылью.
        </p>
        <p>
            По зачётам «Саратовэнерго» можно было получать не только ликвидные товары, но и некоторую арматуру. В Саратовской области был Краснокутский арматурный завод, мы с ним работали ещё во времена «Простора». Он делал латунные вентили и краны,
            которые мы брали в обмен на долги за энергию. Незадолго до кризиса 1998 г. у завода начались трудности, и у нас появилась возможность получить монополию ещё и на его продукцию...
        </p>
    </blockquote>
    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ СТАНИСЛАВ КУРБАТОВ:</strong></p>
        <p>
            Краснокутский завод был третьим в Союзе по объёмам производства латунной арматуры после Бологовского и Гатчины. Я был главным инженером завода. Мы делали 3500 тонн латунных изделий в год, из них 3200 тонн вентилей 15б1п, 15б3р и других, а
            ещё сантехнику всякую, контрагайки, гвозди. Всю зачётную экономику завод жил вольготно, так как наша продукция была востребована у энергетиков, газовиков. Мы спокойно расплачивались за сырьё, перебоев не было. Но в какой-то момент сделали
            ошибку: начали брать на себя чужие долги. Дело в том, что наша котельная обслуживала всех соседей: 11 предприятий. И получилось так, что мы за потребленный котельной газ платим, а они нам за тепло не платят, берут в долг под обещания, что
            вот, мол, скоро станет полегче — а город-то маленький, как не дать? Ну и завод здорово просел. «Энергомашкомплект» мы хорошо знали, он с заводом работал по взаимозачётам с энергетическими компаниями: «заводил» нам эти зачёты и забирал в
            обмен продукцию. И когда у нас начались проблемы с оборотными средствами, мы заключили такой договор: ЭМК снабжал нас металлом, энергией, платил деньги за выполненную работу — и забирал всю изготовленную продукцию. В 1999 г. мы плотно
            поработали и сделали порядка 300 тысяч вентилей по этому договору.
        </p>
        <p>
            Возможно, ЭМК хотел купить завод, но сделать это не было возможности, потому что отдельных акций завода не существовало. Дело в том, что в советское время мы относились к Главпромвентиляции Минмонтажспецстроя. При приватизации данный
            главк преобразовался в ОАО «Климатехника». В это предприятие вошло 6 заводов, включая наш, и каждый был как структурное подразделение, а акции были только самой «Климатехники». Такая вот хитрость генерального директора (который
            впоследствии и хозяином стал). У каждого завода был свой директор, но генеральный директор был один — в Москве.
        </p>
    </blockquote>
    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</strong></p>
        <p>
            Фактически мы взяли Краснокутский завод в управление. Давали им оборотные средства, поставляли туда газ, электроэнергию и забирали всю продукцию. Причём такая продукция получается рентабельной только при массовом её производстве. Поэтому
            мы вынуждены были работать на склад, параллельно налаживая каналы сбыта больших партий латунных вентилей. Процесс этот шёл непросто. И у нас на складе начали накапливаться запасы. Зачёты за газ и электроэнергию мы успешно на завод
            проводили, но сырьё (латунь в слитках), налоги и зарплату приходилось оплачивать деньгами. Мы стали понимать, что совершили стратегическую ошибку, сделав ставку на продукцию, каналы сбыта которой отличаются от наших основных каналов
            сбыта. ЭМК ведь работал, в основном, с крупными прямыми потребителями, которые, конечно, использовали латунные вентили, но не в массовом порядке. Для реализации того объёма вентилей, который требовался для безубыточной работы завода, нам
            необходимо было строить дилерскую сеть. А это процесс небыстрый и непростой.
        </p>
        <p>
            Проблема решилась, к нашему удовлетворению, сама собой. К нам вдруг приехал собственник завода Валентин Владимирович Иванов. Мы тогда скромно сидели в «Теплоремонте», а он весь из себя такой солидный московский менеджер. Ну, освободили
            ему один стул... и он говорит: «Я всех руководителей с завода уволил, они всё развалили, я решил сам управлять заводом, договор с вами я расторгаю, все долги перед вами погашу». И вскоре вправду загасил их... деньгами! Получилось, что мы
            продали зачёты за деньги один в один и сняли с себя головную боль! Такой вот был весёлый эпизод.
        </p>
    </blockquote>
