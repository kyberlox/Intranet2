    <h2 class="page__title">Глава 9. Рождение САЗа</h2>
    <p>
        ИФАЗ, эксклюзивные права на продукцию которого сыграли важнейшую роль в становлении ЭМК, тем не менее трудно считать «собственным» производством. Юридически акции ИФАЗа принадлежали украинскому «ИНТЭКО», да и фактически завод управлялся из
        Украины. Производство, организационно вписанное в структуру «Энергомашкомплекта», появилось «естественным образом» только в 2001 г. и, конечно же, в Саратове. Хотя ещё несколькими годами ранее ЭМК имел возможность обрести собственные
        производственные мощности в Красном Куте<sup id="fnref:28"><a class="footnote-ref" href="#fn:28" role="doc-noteref">1</a></sup
        >, но предпочёл ограничиться схемой отношений, которая называлась тогда «толлингом».
    </p>
    <p>
        Начало нулевых годов ознаменовалось возрождением российской производственной сферы, оказавшейся после кризиса 1998 г. в куда более благоприятных условиях, чем до него. Начался экономический рост, причём «здоровый», а не обусловленный высокими
        ценами на нефть. Среди производителей воцарился осторожный оптимизм. Заводы, пережившие «лихие 90-е», учились работать в нормальных рыночных условиях и вскоре стали проявлять свой потенциал (у кого он был). Появились и молодые
        производственные фирмы, в том числе в арматуростроении. Они пока ещё не могли всерьёз конкурировать с заводами, действующими с советских времен, поэтому искали рыночные ниши, крупными производителями не освоенные.
    </p>
    <p>
        На рынке арматуры «темой» в эти годы были стальные шаровые краны, спрос на которые быстро рос. В СССР они почти не производились, а в 1990-е гг. стало не до них. В то же время на Западе производство шаровых кранов быстро нарастало, и импорт
        инициировал спрос на них со стороны российских потребителей. Это была удачная ниша для нового производства. Но чтобы его начать, мало понимать «тему». Нужны ещё люди — специалисты, способные организовать производство и управлять им.
    </p>
    <p>
        В ЭМК нужные люди появились, в общем-то, случайно. Производственное направление в компании сформировалось в том же стиле, что и основное торговое: по принципу «всё только по мере необходимости». Не было заказанных в каких-нибудь ГИПРО
        проектов, не было каких-то выверенных планов. Был лишь энтузиазм, помноженный на чёткое видение целей.
    </p>
