            нуля дало нам много знаний и опыта по части решения организационных проблем, с которыми можно при этом столкнуться. И в дальнейшем, оценивая варианты покупки действующих производств (а нет, пожалуй, ни одного арматурного завода в России,
            к которому бы мы не приценивались), мы уже держали в голове, во что может вылиться организация такого же производства своими силами. Это в конце концов помогло нам прийти к выводу, что свой завод построить с нуля почти всегда выгоднее,
            чем покупать чужой. И через несколько лет мы создали с нуля второй завод, а затем и третий...
        </p>
