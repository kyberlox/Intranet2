            То, что мы начали вместе с ЭМК работать — как раз и вызвало реорганизацию завода. Мы же в Москву документы предоставляли, где всюду был указан «Энергомашкомплект» и номер его телефона. Из Москвы, из центрального офиса в ЭМК позвонили и
            спросили, сколько стоят латунные вентили. И кто-то им ответил: «А-а, это которые наш завод делает...» Эти слова настолько взбесили господина генерального директора, что он принял резкие меры. Он решил, что за его спиной какие-то дела
            крутят и пытаются у него завод увести (хотя ситуация, видимо, была не совсем такая). Он вроде как стал управлять сам, директора отодвинул, а в конце концов поменял и директора, и замов — всю команду. Меня только оставил. Директором
            поставил женщину... Ну и начался на заводе матриархат. Главный механик — женщина, все — женщины, даже транспортный отдел возглавляла женщина. Это было что-то!
        </p>
    </blockquote>
    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</strong></p>
        <p>
            Итак, от Краснокутского завода мы удачно избавились. Но собственное производство в Саратове у нас вскоре всё же появилось. Его организация не была для нас самоцелью, просто так сложилось. В то время быстро рос новый сегмент рынка: шаровые
            краны, мы искали, где их можно брать, и вдруг появилась возможность попробовать производить их самим.
        </p>
        <p>
            Дело было так. Звонит мне товарищ из администрации города и говорит: «Ко мне тут пришли люди, предлагают производство наладить газового оборудования — может, тебе будет интересно?» Говорю: «Присылай». Приходят два человека, показывают
            бизнес-план по созданию производства регуляторов, ГРПШ<sup id="fnref:29"><a class="footnote-ref" href="#fn:29" role="doc-noteref">2</a></sup> и прочего. Я его просматриваю по диагонали... и вдруг вижу, что там упоминаются «шаровые краны»
            (они входят в состав ГРПШ). Я говорю: «А вы шаровой кран сделать можете?» — «Можем!» — «Вот сделайте кран, и тогда посмотрим, потому что ваши шкафы я продавать не умею».
        </p>
        <p>
            27 декабря 2000 г. мы зарегистрировали новую компанию под названием «Саратовский арматурный завод». Вячеслав Сергеевич Степанов и Лев Трофимович Бугаенко — те самые два человека — стали соучредителями, а директором я пригласил Льва
            Алаева.
        </p>
    </blockquote>
    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ ЛЕВ АЛАЕВ:</strong></p>
        <p>
            После нашего знакомства с Кошкаревым прошло несколько лет. К этому времени зачётные схемы свернулись, в «Саратовэнерго» мне стало как-то уже неинтересно. И тут звонит Кошкарев: «Хочешь быть директором завода?» А я, честно говоря, тогда не
            знал, что такое токарный станок. И что такое фрезерный, если честно, тоже не представлял. Но он говорит: «Да ладно, не бойся, есть специалисты, они всё знают!»
        </p>
        <p>
            Специалистами были Степанов и Бугаенко. Они пришли к Кошкареву со своим бизнес-планом, Кошкарев им предложил делать арматуру, они согласились. Но Кошкареву нужен был человек, которому он доверяет. Например, я. Меня Кошкарев знал, а их не
            знал. Поэтому решил так: «Деньги будут у тебя, и ты будешь отвечать мне за каждую копейку». Он мне нарисовал перспективы... Авантюра, конечно. Но мы тогда молодые были, можно было и рискнуть.
        </p>
    </blockquote>
