<h5>РАССКАЗЫВАЕТ СЕРГЕЙ ЛЕВИН:</h5>
<blockquote>
    <p>
        С Кошкаревым мы учились вместе, но когда всё это началось в начале 1990-х гг., он сразу ушёл, а я остался в
        институте. Диссертацию сделал. Потом появились проблемы со здоровьем у папы, денег почти не платили, и я
        пошёл брокером на биржу.
        В институте только числился, а так всё бегал-бегал, торговал чем-то, но как-то бестолково всё.
    </p>
    <p>
        А потом мы неожиданно с Виталием пересеклись. Он меня спрашивает: «Ты чем сейчас занят?» — «Ну так, —
        говорю, — помаленьку», — «Слушай, а пошли к нам! У нас тут новое интересное направление, нужно искать
        покупателей, спрос будет большой,
        хорошо заработаем, 11ч3бк будем продавать!» А я был далёк от всего этого, удивился слегка: «Какие три быка?
        Ты что, в животноводство подался?» Ну ладно, думаю, в принципе, могу и быков продать. «Да нет, — говорит, —
        это краны такие
        чугунные, на трубу ставятся».
    </p>
    <p>
        Вот так я занялся арматурой. Сначала работал больше как водитель, потом понемногу вникать начал: что такое
        задвижка, а что — обратный клапан. И как-то оно хорошо улеглось в голове, всё же техническое образование. А
        то люди сейчас
        некоторые приходят и не могут разобраться — гуманитарии, не хватает технических знаний. Мы тоже учились
        другому: ну там, плуг, сеялка, комбайн и всякое прочее. Но берёшь книжку — и читаешь. И всё понятно. Техника
        — она техника и есть.
    </p>
    <p>
        Начали с кранов, тех самых, которые «три быка», потом другое пошло: задвижки, клапаны. Ездил договаривался
        больше Сергей Великанов, он с арматурой через маму был знаком. А Виталий банковскими делами занимался, счета
        выписывал, проверял
        оплату.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</h5>
<blockquote>
    <p>
        Нас было трое сначала: Великанов, Тоскарь, Кошкарев, хотя Дима, будучи учредителем «Простора», продолжал
        работать врачом и лишь помогал нам от случая к случаю. Обязанности примерно так поделились: Сергей Великанов
        сделки делал, а я
        «внутреннюю кухню» вёл. Вскоре к нам ещё несколько человек присоединилось ( Небывалов, Левин, Эмануилов, а
        потом и Тоскарь), и мы уже стали работать вшестером.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ДМИТРИЙ ТОСКАРЬ:</h5>
<blockquote>
    <p>
        Виталий полностью переключился на бизнес вторым из нас троих, оставив работу в институте. Но и я, будучи
        учредителем, выполнял какие-то задачи: с кем-то поговорить, что-то куда-то отвезти. Мы тогда все, от
        директора до водителя, работали
        на складе, на разгрузках-погрузках. В 1993 г. я тоже ушёл из института и перешёл в «Простор». К этому
        времени фирма уже сложилась: в ней работали также Небывалов, Левин и Эмануилов.
    </p>
    <p>
        Офис у нас был в подвале дома, где жил Великанов. Подвал сдавался в аренду, одну его комнату занимала
        оптовка Яши Эстермана, другую — известный в городе криминальный авторитет, пожарники, а самая маленькая
        комната была наша. Там стояли
        три стола, и больше ничего не было. А сидело нас в ней человек шесть-семь. Поначалу каждый договор, каждый
        документ — это было для нас очень важно и ново. Первое время мы работали почти бесплатно, позже появился
        бартер, и мы получали
        обувь, электроприборы, продукты, спирт. Транспорта тоже поначалу не было, первая наша машина — это наёмное
        такси, которое возило Великанова по объектам. Затем пошли первые поставки отводов и труб Саратовским
        Теплосетям, первым нашим
        крупным заказчиком стало «Саратовэнерго», когда Виктор Александрович Макаров согласился заключить с нами
        договор. И пусть договор этот был не на самых выгодных для нас условиях, стало ясно, что работа началась
        по-настоящему.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ВИКТОР АЛЕКСАНДРОВИЧ МАКАРОВ:</h5>
<blockquote>
    <p>С Кошкаревым мы знакомы ещё с тех пор, когда его фирма называлась «Простор». Сошлись мы не на арматуре,
        арматура была уже после. А сначала были трубы.</p>
    <p>
        Я тогда работал начальником отдела металлов «Саратовэнергоснабкомплекта» ( снабженческое подразделение
        «Саратовэнерго»). Проблема у нас была в том, что в 1992 г. цены на трубы, которые для тепловых сетей нужны в
        огромных объёмах, резко
        пошли вверх. А «Простор» имел связи с Украиной. И давал цену на 20–30, а то и на 40% ниже, чем российские
        заводы.
    </p>
    <p>
        По тем временам это, конечно, подозрительно было: там заводы, а тут какой-то «Простор» непонятный. Но я
        приносил прейскуранты с Каменск-Уральска, с Выксы, и всегда, когда заявку подписывал, указывал: тут такая-то
        цена, а тут такая,
        рекомендую брать у «Простора».
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</h5>
<blockquote>
    <p>
        В начале 1993 г. Великанов добыл информацию о потребностях «Саратовэнерго» в трубах и нашел в Украине
        дешёвый вариант — Харцызский трубный завод<sup id="fnref:note4"><a class="footnote-ref"
               href="#fn:note4"
               role="doc-noteref">4</a></sup>. Инфляция тогда была страшная, более 20% в месяц. Видимо, наши трубные
        заводы согласованно поднимали свои цены, а Харцызск с этим «отставал». И когда мы вышли в «Сарэнерго» с
        предложением, оказалось, что наша цена гораздо ниже, чем у
        российских заводов. Заказчик рискнул, перевёл нам предоплату (а тогда иных вариантов расчётов не знали), мы
        тут же на эти деньги купили трубу и осуществили поставку. Всё прошло отлично, и началась постоянная работа с
        «Сарэнерго».
    </p>
    <p>
        Потом появилась тема отводов. Нам пришла заявка СПКТС (Саратовское предприятие коммунальных тепловых сетей).
        Оказалось, что в Саратове негде было купить отводы! Мы стали искать поставщика — и нашли (в Сысерти,
        кажется). Привезли
        контейнер, продали втридорога и начали заниматься отводами на постоянной основе.
    </p>
    <p>
        «Украгротехсервис» Великанов нашёл, обзванивая по справочнику потенциальных заказчиков на Украине.
        Выяснилось, что у этого предприятия просто огромная потребность в газовых клапанах и регуляторах
        производства саратовского «Газаппарата».
        Но в России прошла денежная реформа, оплатить российскими рублями «Украгротехсервис» не мог, и представители
        предприятия предложили нам бартер. Мы решили рискнуть. Мы уже понимали, что можем с Украины взять трубу для
        «Сарэнерго». Также
        «Сарэнерго» готово было брать арматуру ИФАЗа. Продукция «Промприбора» из Ивано-Франковска была на нашем
        рынке страшным дефицитом. В общем, мы ввязались в это дело! Купили на свои деньги первую партию регуляторов,
        отправили её на Украину —
        и началось! «Украгротехсервис» платило деньги украинским поставщикам по договорам, которые физически от его
        имени заключали мы, мы же контролировали отгрузку, вели переговоры о сроках, ценах... В общем, делали всю
        техническую работу. Это
        заставляло нас много времени проводить на Украине.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ВИКТОР АЛЕКСАНДРОВИЧ МАКАРОВ:</h5>
<blockquote>
    <p>
        С поставками от «Простора» всегда было всё нормально, это понравилось руководству, и поэтому мы с этой
        организацией работали всё теснее. Мы закупали у них много продукции: нам же километры теплосетей нужно было
        менять. Сначала только
        трубы, потом арматуру тоже стали закупать. Основная арматура у нас была Чеховского завода, но и с Украины
        можно было что-то брать: там был Ивано-Франковский завод. Объёмы росли. Цены были приемлемые. Потом
        «Простор» превратился в
        «Энергомашкомплект», и мы уже с ним продолжали работать. И так было вплоть до 1997 г., когда я сам перешёл
        на работу в ЭМК.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</h5>
<blockquote>
    <p>
        Начав с простеньких газовых кранов, мы шаг за шагом наращивали ассортимент и объёмы поставок. Деловые связи
        росли как снежный ком, каждый новый контакт давал новую информацию о потребностях покупателей, мы искали,
        чем их удовлетворить, —
        и находили новые связи и возможности. Вскоре мы могли закрыть текущую потребность в арматуре практически
        любого небольшого заказчика, стали выходить на более крупных потребителей, которым была нужна также
        специальная арматура, — мы начали
        развивать отношения и с её производителями. Мы всё лучше разбирались в арматуре и в структуре её рынка. Два
        года работы «Простора» стали для нас отличной школой бизнеса, превратив из наивных студентов в опытных
        коммерсантов. «Простор»
        теперь вроде бы прочно стоял на ногах. По прошествии лет понятно, что на самом деле он был как дом без
        фундамента. Ведь покуда денег нет, кажется, что главное — навалиться всем миром и побыстрее их заработать.
        Но когда они появляются —
        оказывается вдруг, что с ними возникают и новые проблемы, которых поначалу никто не ожидал...
    </p>
</blockquote>
