<h5>РАССКАЗЫВАЕТ АЛЕКСЕЙ ПЕТРОВИЧ КОШКАРЕВ:</h5>
<blockquote>
    <p>
        Во времена перестройки я работал в «Тепловых Сетях», и когда в 1987 г. началось кооперативное движение,
        сразу же решил создать кооператив. Но создавать их тогда можно было только при действующих предприятиях с
        разрешения руководства. Я
        стал у директора просить разрешение, он не давал. А при заводе «Сардизель» появился кооператив «Грань», мне
        предложили стать его председателем, и я согласился. Через полгода оказалось, что завод нас жестоко обманывал
        с ценами, и мы ушли
        на вольные хлеба. Стали заниматься ремонтом и обслуживанием тепловых сетей. После принятия закона «О
        кооперации» поменяли устав, а название сменили на «Теплоремонт».
    </p>
    <p>
        Когда Виталий, вместо того чтобы в институте наукой заниматься, тоже ушёл деньги зарабатывать — я не то что
        был не против, я только приветствовал. Я ж его этому и учил. А что он торговлей занялся, так ведь это было
        гораздо выгоднее! У
        нас-то в «Теплоремонте» прибыли особой не было. Мысль была такая, чтобы спокойно дожить до старости. А им,
        молодым, нужно было другое.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</h5>
<blockquote>
    <p>
        Вскоре оказалось, что, к сожалению, Санэпидемстанция нам заказов приносить не сможет. Пару проектов мы
        сделали, но толком дело не шло. И пока не появилась арматура, фирма «Простор» вела этакое «полумёртвое»
        существование. А арматура
        появилась так. У Сергея Великанова мама всю жизнь проработала в Госснабе и занималась именно арматурой, так
        что Сергей немного знал эту отрасль. И как-то на одной из дружеских посиделок он нам говорит, мол, есть
        идея! «Знаю, где купить
        дёшево партию арматуры, знаю, куда её продать дорого, но денег нет и фирмы нет».
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ДМИТРИЙ НЕБЫВАЛОВ:</h5>
<blockquote>
    <p>
        Мы все учились в одной школе. (Окончить 37-ю школу — великое дело!) Общались и после школы. Я отслужил в
        армии, в 1988 г. вернулся, поработал два года в «Нефтемаше» (нынешний «Сапкон»), а потом, когда пришла
        перестройка и начались
        проблемы с деньгами, меня позвал к себе Сергей Великанов. Он был экспедитором в фирме «Газприбор», связанной
        с газовым оборудованием, пошёл на повышение и пригласил меня на своё место. И я поступил туда на работу.
    </p>
    <p>
        Осенью 1992 г. у Великанова появилась идея. «Газприбор» работал с газовым оборудованием, но с арматурой не
        работал, хотя вроде бы продукция смежная. Почему бы ей не заняться? Он пришёл с этой идеей к руководству, но
        его не поняли. И тогда
        он пришёл в «Простор».
    </p>
    <p>Новая идея пришлась ко двору. Начиналось всё с одной партии. Помню, мы её разгружали в гараже Тоскаря весь
        вечер, с «газончика» скидывали — ещё не холодно было. Большая была партия!</p>
    <p>А потом пошли первые договоры. Оказалось, что мы очень удачно попали в тему! Хозяйственные связи порвались,
        заводы сами найти себе арматуру не умели. А мы находили, её много было на складах ещё по советским ценам.
    </p>
    <p>И вскоре Великанов &gt; ушёл из «Газприбора» в «Простор». А через некоторое время в «Простор» перешёл и я.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</h5>
<blockquote>
    <p>
        В той первой сделке речь шла о латунных газовых кранах. А ведь Саратов — центр газовой отрасли, здесь такие
        краны всем нужны. По рыночным ценам вся партия кранов могла быть продана минимум за миллион. Причём у нас
        был готовый сбыт —
        бывшая фирма Великанова готова была взять крупную партию.
    </p>
    <p>
        Но сначала их надо было выкупить в Госснабе. Денег для этого нужно было 60 тысяч рублей. По тем временам это
        были большие деньги! <sup id="fnref:note3"><a class="footnote-ref"
               href="#fn:note3"
               role="doc-noteref">3</a></sup> У нас их,
        конечно, не было. И мы все кинулись их искать. Отец обежал всех своих коллег-кооператоров — никто не дал. И
        тогда мы обратились к моему дяде, который работал директором саратовского «Энергосбыта». Мне как-то не очень
        удобно было к нему
        обращаться, всё же важный был человек. Но поскольку других вариантов не было... На удивление, он сразу
        согласился помочь. Перечислил нам на счёт предоплату за что-то. И мы на эти деньги купили партию кранов, тут
        же часть продали за 120
        тысяч, вернули предоплату... и с тех пор начали заниматься арматурой.
    </p>
    <p>
        Мы потом долго ещё эти краны продавали. За ними часто из Казахстана приезжали, причём платили наличными. Ну,
        мы же не можем отказать клиенту. А как отгрузить, как оформить — под учётом всё? И мы шли на базар,
        буквально за копейки эти
        краны там скупали... Я этого не могу забыть: тысячи процентов прибыли! Такие были времена...
    </p>
    <p>
        Вот так всё и началось. Как это часто бывает — с одной сделки. А поскольку её предметом была арматура, и мы
        вдруг увидели, что это интереснейший товар, ликвидный, она всем нужна — мы решили ей заниматься. Многие
        тогда торговали
        ширпотребом — тем, о чём знали в обычной жизни. А такие вот ниши — они как бы в стороне, они долго почти
        пустые были.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ЛЮДМИЛА ОСИПОВНА МАТВЕЕВА:</h5>
<blockquote>
    <p>
        С Виталием Кошкаревым мы познакомились ещё в 1992 г. Я тогда работала в Госснабе, в отделе арматуры. К тому
        времени Госснаб уже потерял свою силу. Зарплату платили минимальную, никаких премий. Все понимали, что дело
        идёт к распаду. В
        советское время наше отделение Госснаба обеспечивало две области: Пензенскую и Саратовскую. Предприятия
        Пензы и Саратова брали арматуру только у нас. Естественно, со всеми этими предприятиями мы были очень хорошо
        знакомы. Потом фондовые
        поставки отменили, закупки стали свободными: кому сколько надо — тот столько и закупал. И Госснаб вроде бы
        стал не нужен. Но у нас ведь много всего было на складах. В наш состав входила Зоринская база, а там было
        всё, начиная от тряпочек
        и заканчивая автомобилями. И каждый, кто хотел что-то купить, мог к нам в Госснаб прийти и купить.
    </p>
    <p>
        Вот тогда мы с ребятами и познакомились. Они тоже к нам пришли. Их было трое: Виталий Кошкарев, Серёжа
        Великанов, а основной парламентарий у них был Дима Тоскарь. Ох, он такой человек коммуникабельный, такой
        шутник, разговором мог к себе
        расположить любого! У нас в отделе были одни женщины, а он врач был, окончил мединститут, и этим, видимо,
        подрабатывал. И как-то раз зашёл он к нам в отдел и говорит: «Вы имейте в виду, если вдруг надо будет что-то
        по медицинской линии,
        ну там справочку какую для бюллетеня, так я могу выписать. Всё как положено, с печатью! Только, — говорит, —
        есть одно маленькое обстоятельство. Справочка будет... из венерологического диспансера». Мы там просто упали
        все от смеха! И
        потом каждый раз, приходя, он как бы невзначай, невинно так спрашивал: «Ну что, справочку-то надо кому?» «Да
        нет, — говорим, — вроде все здоровы». «Это хорошо, — говорит, — но если что — обращайтесь». Вот такая шутка
        была. В общем, он
        всех нас обаял.
    </p>
    <p>
        Но ребята, конечно, не только пошутить приходили. Им хотелось сделать свой бизнес. Они решили заниматься
        арматурой, но ничего про неё тогда не знали. Приходили к нам и спрашивали: «А вот это что? А это?» Мы им
        рассказывали. И как-то они
        ко мне обращались всё больше. Может, потому, что я всегда готова была им что-то подсказать. Я им даже дала
        свои каталоги промышленной арматуры. Они быстро учились, им всё легко давалось, они очень грамотные.
        Главное, что им очень хотелось
        научиться. Для ребят это было важно, они знали, что если у них всё получится — это даст им средства к
        существованию.
    </p>
    <p>
        У них было своё предприятие, называлось «Простор». Но базы у них не было, складов не было — и вот они нас
        попросили небольшой уголочек на складе выделить, сколько-то там метров квадратных со стеллажами. Мы им пошли
        навстречу. Они этот
        уголочек оформили на себя, перекладывали туда то, что у нас покупали, а если ещё где-то покупали, тоже
        привозили и туда складывали. И естественно, все покупатели знали, что оборудование хранится на Зоринской
        базе, что здесь никакого
        обмана нет. А в то время такое надувательство было, вы не представляете! Каждый второй был обманщик! А
        ребята всегда честно работали, мне это нравилось.
    </p>
</blockquote>
