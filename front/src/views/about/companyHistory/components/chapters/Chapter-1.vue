<h1 class="page__title">Глава 1. ПРОСТОР</h1>
<p>
    Четверть века назад, в декабре 1991 г., закончилась история Союза Советских Социалистических республик (СССР). И
    тогда же, в декабре 1991 г., возникла фирма «Простор», с которой началась история компании «Энергомашкомплект»
    (ЭМК). Совпадение
    случайно лишь отчасти. В нём отразилось наступившее тогда в нашей стране Время Перемен. Становление ЭМК
    происходило не просто на фоне стремительных изменений в жизни страны, сопровождавших мучительное рождение новой
    эпохи, но и благодаря им.
    История — и страны, и компании — это ведь не только последовательность дат, но ещё и дух времени, проступающий
    даже в самых обычных поступках и решениях.
</p>
<p>
    А время было смутным. В последние годы своего существования Советский Союз испытывал огромные экономические
    трудности. В середине 1980-х гг. к руководству страны пришло понимание, что проблемы связаны со сложившимся в
    СССР экономическим
    укладом: халатность и мелкое воровство стали обыденностью «социалистического труда», плановое хозяйство было
    неповоротливым в части технического развития и обновления ассортимента.
</p>
<p>
    Перестройка, запущенная М.С. Горбачевым в 1987 г., как раз и являлась попыткой преодолеть эти трудности. Были
    разрешены кооперативы, в стране появились первые частные предприниматели, но условий для успешной работы им
    создано не было,
    Советский Союз продолжал жить по законам плановой экономики. Затем была объявлена «гласность»: отменена цензура,
    прекращены преследования по идеологическим мотивам. Общество забурлило. На фоне выпестованной за десятилетия
    «железного занавеса»
    зависти к якобы «загнивающему» Западу люди ждали и желали перемен, причём среди активной части населения
    распространилась иллюзия, что стоит только перейти на рыночный уклад экономики — и все проблемы будут быстро
    решены.
</p>
<p>
    В 1990 г. по Восточной Европе прокатилась волна «бархатных революций», начался «парад суверенитетов» республик
    СССР, в некоторых из них вспыхнули межнациональные конфликты. Союзное государство быстро теряло власть. В
    октябре 1990 г. Россия
    объявила о верховенстве своих законов над союзными, а 25 декабря вышел закон РСФСР «О предприятиях и
    предпринимательской деятельности», полностью легализовавший в России частную собственность. Была резко упрощена
    процедура регистрации частных
    предприятий, и они стали возникать как грибы после дождя. Августовский путч 1991 г. выглядел последней
    обречённой попыткой сдержать победный марш новой эпохи. О цене этой победы в то время мало кто задумывался. Ведь
    на самом деле общество
    психологически не было готово к рынку. Изменить уклад экономики в огромной стране — не поле перейти!
</p>
<p>
    2 января 1992 г. в России была введена свобода торговли и объявлена либерализация цен, которые до этого
    полностью регулировались государством. Распад СССР и прекращение функций Госплана<sup id="fnref:note1"><a
           class="footnote-ref"
           href="#fn:note1"
           role="doc-noteref">1</a></sup>
    вызвали массовый разрыв хозяйственных связей между предприятиями. Тут же началась безудержная инфляция, которая
    по итогам 1992 г. составила 2600%, то есть цены выросли в 27 (!) раз. (В 1993 г. они выросли ещё почти в 10 раз,
    в 1994 — в три
    раза, и лишь затем инфляция притормозила до приемлемого уровня.) Российские заводы, на которых никогда не было
    полноценных коммерческих служб, не понимали, как работать в условиях, когда некому дать им указаний, куда, что и
    почём продавать.
    Многие по инерции отпускали свою продукцию по старым ценам, хотя если поискать, её можно было продать гораздо
    дороже. А подразделения Госснаба, с баз которого раньше комплектовалась вся советская промышленность, весь 1992
    г. стойко выполняли
    инструкцию, устанавливающую наценку 25% к закупочной цене<sup id="fnref:note2"><a class="footnote-ref"
           href="#fn:note2"
           role="doc-noteref">2</a></sup>. Хотя рыночные цены к концу года были уже в десятки раз выше рассчитанных
    таким образом госснабовских отпускных.
</p>
<p>
    В экономике воцарился хаос. Одним заводам позарез нужна была продукция, которую другим заводам некуда была
    девать, — и они не умели найти друг друга и договориться. Это породило массу возможностей для торгового
    посредничества. Многие частные
    компании, возникшие в 1991–1992 гг., поначалу пытались заниматься производством или услугами, но быстро
    выясняли, что торговля в таких условиях приносит куда больше прибыли. И на место, освобождённое рухнувшей
    плановой системой поставок,
    хлынула волна вчерашних студентов. Оборванных хозяйственных связей хватало на всех.
</p>
<p>
    Нашему поколению, родившемуся в 1960-х гг., в каком-то смысле повезло. Когда на страну обрушились рыночные
    реформы, мы были уже достаточно взрослыми, чтобы самим строить свою жизнь, ни на кого не полагаясь. В то же
    время, мы были ещё
    достаточно молоды, полны сил и энергии, а главное, не затянуты болотом насиженных тёплых мест в плановом
    хозяйстве и не отягощены штампами идеологии коммунистического труда. Нам не было страшно, как многим из старших
    товарищей, перед лицом
    перемен. Наоборот, нам было интересно и весело! Было ощущение необъятного простора впереди: все пути открыты!
    (Может быть, отсюда и название фирмы-предшественника ЭМК?) Хотелось всеми силами с поднятым забралом ввязаться в
    новую, не обретшую
    ещё четких форм рыночную бурю. Конечно, преуспели не все. Возможности ведь мало иметь, ими ещё нужно уметь
    воспользоваться. Тем, кто не сумел, не стоит обижаться и жаловаться, мол, «не повезло».
</p>
<p>История «Простора» начиналась как история трёх друзей, типичная для мировой литературы от русских былин до
    Ремарка. Но в жизни сюжеты обычно бывают сложнее, чем в книгах...</p>
<h5>РАССКАЗЫВАЕТ ДМИТРИЙ ТОСКАРЬ:</h5>
<blockquote>
    <p>
        Мы трое учились в 37 школе города Саратова: я, Сергей Великанов и Виталий Кошкарев. Дружили, вместе
        проводили свободное время, праздники отмечали вместе, на дачу ездили. После школы я поступил в медицинский
        институт, Виталий — в институт
        механизации сельского хозяйства, а Великанов — в экономический на заочное отделение и начал работать в
        структуре «Агропромстроя». После окончания института я поступил в интернатуру, а Виталий устроился в
        проектный институт.
    </p>
    <p>
        Далее мы планировали строить свою жизнь так, как её принято было строить в советские времена. Я неплохо
        учился и намеревался после интернатуры пойти в городской диспансер, а затем — на кафедру кожных болезней.
        Виталий окончил учёбу с
        красным дипломом и думал о защите кандидатской. Но в 1991 г. мы начали задумываться о других возможностях,
        стали поговаривать между собой о том, что, наверное, надо предпринимать какие-то самостоятельные шаги,
        что-то искать новое в смысле
        заработка, так как понятно стало, что на «плановом» пути будущее нас ждёт, мягко говоря, сомнительное.
        Первым такой шаг сделал Сергей, уйдя в частную компанию «Газприбор», которая занималась поставками
        котельного и газового оборудования.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</h5>
<blockquote>
    <p>
        В 1988 г. я окончил институт и по распределению отправился в «Гипропромсельстрой». Работал в отделе, который
        в числе прочего занимался проектами предельно допустимых выбросов в атмосферу. Работа несложная: приходишь
        на завод, смотришь,
        какое там есть оборудование, считаешь по определённым методикам — и на выходе получается некий продукт,
        называемый «проектом ПДВ». Сегодня каждое предприятие обязано иметь такой проект по закону «Об охране
        окружающей среды». Тогда это
        требование только появилось, и предприятия должны были где-то такие проекты купить. Но никто ещё их не
        делал. А мы — делали!
    </p>
    <p>
        Уйдя из «Гипропромсельстроя» в аспирантуру, я всё время думал, чем бы таким заняться в плане заработка. И
        решил, что эта вот тема может принести деньги. Вроде всё складывалось. Кто будет делать проекты? Мои бывшие
        сослуживцы по отделу!
        Они же всё равно это умеют и всегда готовы подработать на стороне. А кто нам будет приносить заказы? Тут я
        вспомнил, что у моего друга, Димы Тоскаря, папа — зам. главного врача Cанэпидемстанции. Мне почему-то
        казалось, что именно эта
        структура должна наказывать заводы за отсутствие проектов ПДВ. И вот мы с Димой создали фирму, я придумал ей
        название — «Простор» — и история началась
    </p>
</blockquote>
