    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ ДМИТРИЙ ТИМОФЕЕВ:</strong></p>
        <p>
            С Виталием мы знакомы со школьных времён. Наши родители были соседями по даче, так что летние каникулы мы
            часто проводили вместе. И потом друг друга из виду не теряли: встречались, общались. Я видел, какую он
            создаёт компанию, и время от
            времени ему предлагал: «Как будет новое направление — возьми меня на работу». С арматурой я знаком не был,
            но в своё время работал конструктором на авиационном заводе, для меня чертеж крана или задвижки прочитать —
            не проблема, это ж не
            самолёт.
        </p>
        <p>
            И вот в 2002 г. Виталий мне звонит: «Приходи!» Я пришёл, и он предложил мне попробовать заняться развитием
            отношений с проектными институтами. Суть работы состояла в том, чтобы передавать проектантам подробную
            информацию о продукции наших
            предприятий с той целью, чтобы эта продукция закладывалась в проекты, которые они делают. Я попробовал, и
            вроде получилось.
        </p>
        <p>
            В интернете, в «жёлтых страницах», во всех источниках, которые только есть, я искал те институты в России,
            Украине, Казахстане, которые проектируют системы, где есть трубопроводы. Звонил секретарю, представлялся,
            объяснял, что мы
            поставляем трубопроводную арматуру, просил связать меня с отделами, которые ей занимались, потом звонил в
            эти отделы, договаривался и высылал им курьерской почтой наши каталоги. Заводских каталогов поначалу не
            хватало, я сам занимался их
            ксерокопированием, стоял часами у ксерокса. За день иногда рассылал эти толстые стопки бумаги по 20 адресам.
        </p>
        <p>
            Ведь проектировщики в то время очень страдали от недостатка информации. Они ходили по выставкам, собирали
            огромные сумки с каталогами, тащили эти тяжеленные сумки до поезда... Мы, наверное, были не первыми, кто
            стал им сам рассылать свои
            каталоги, но одними из первых. А если с ними начинаешь нормально общаться: по запросу быстро предоставляешь
            какую-то дополнительную информацию, которая им нужна для работы — они идут навстречу и включают твою
            продукцию в свои проекты.
        </p>
        <p>
            Проблема была в том, что мы «двигали», в основном, украинскую арматуру. И хотя отношения между странами в то
            время были нормальные, все равно возникали вопросы типа: «А с чего это мы будем ставить в проект
            Ивано-Франковск вместо Пензы?» И
            мы начали применять схему, которую Дима Тоскарь в Украине освоил: заключали с институтом договор, по
            которому, если наша арматура включена в проект и была затем у нас куплена, мы определённый процент выручки
            перечисляем институту. Для
            этого институт ежеквартально высылал нам спецификации с указанием ответственных лиц предприятий, в чьи
            проекты включена наша арматура (что само по себе ценно). Обычно проектанты к таким договорам относились
            поначалу скептически: «Всё
            равно ж ничего не заплатите!» Они ведь никак проверить не могут, получили мы выручку или нет, и вынуждены
            верить нам на слово. Но мы платили! Потому что не заплатишь раз, не заплатишь два, особо на этом не
            озолотишься, а отношение к тебе
            будет соответствующее. А мы хотели на длительной основе сотрудничать.
        </p>
    </blockquote>
    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</strong></p>
        <p>
            Покупка «Тулаэлектропривода» стала новым мощным двигателем нашего развития. Даже сам факт этой покупки
            понятным образом изменил к нам отношение крупных участников рынка. Став совладельцами «Тулаэлектропривода»,
            мы окончательно
            почувствовали себя игроками высшей лиги на российском рынке арматуры.
        </p>
    </blockquote>
    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ ОЛЕГ БУРОВ:</strong></p>
        <p>
            Пока «Тулаэлектропривод» был в кризисе, клиенты получали тульские приводы откуда попало. Естественно, когда
            мы объявили, что имеем отношение к этому заводу, все в нашу сторону развернулись. Поменялось отношение к
            нам, удалось сломать
            ситуацию на ряде объектов, где люди до этого закупали арматуру и приводы отдельно.
        </p>
        <p>
            Был случай, когда какие-то ребята привезли одному из наших клиентов тульские приводы, а он взял и сделал нам
            запрос насчёт них. Мы спросили завод — выяснилось, что эти приводы завод не изготавливал. А ребята были
            близкие к первым лицам
            клиента, уверенные в себе, наглые. Они появились через месяц, открыли ногой дверь: «Ну, что там с приводами?
            Где наши деньги?» — «О, как хорошо, что вы пришли! А вас тут ждут. У службы безопасности к вам вопросы есть:
            где вы взяли эти
            приводы?» И — всё. Эти люди прямо из приёмной исчезли и больше их никто не видел. Даже приводы не забрали.
        </p>
    </blockquote>
    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ АЛЕКСЕЙ АЛЁШИН:</strong></p>
        <p>
            Покупка «Тулаэлектропривода» — это сильный ход! Может, так сошлись звезды, что стало возможно этот завод
            подобрать, но нам это принесло очень много прибыли. Не за счёт продажи приводов: по-отдельности приводы
            покупали редко. А за счёт
            того, что клиентам удобнее сразу комплектовать закупаемую арматуру. Тем более раньше, например, стальные
            задвижки одного диаметра могли требовать разных типов приводов. Скажем, заказчик где-то посмотрел в
            справочнике, что к пензенской
            задвижке нужен привод типа А, но задвижку купил почему-то юго-камскую, к которой тип Б. Привод не подходит,
            он в панике, а тут мы, такие грамотные ребята: «Что вы мучаетесь? Давайте мы вас закомплектуем! Вам нужна
            задвижка с фланцами, с
            приводом, с болтиками и с прокладочкой? Не вопрос! Пишем в заявке: электроприводная задвижка в комплекте. И
            мы зуб даем, что всё подойдёт, а не подойдёт — так мы вам поменяем, не переживайте!» И к ним приезжает всё
            сразу, аккуратно
            запакованное: вот в ящичке гаечки со шпилечками, вот прокладочки, да ещё про запас одна лишняя. (Мы даже
            покупали паронит в листах и вырезали из него прокладки.)
        </p>
        <p>
            У нас были таблицы соответствия, мы быстро руку набили в подборе приводов. И не только в этом. Вот,
            например, звонит клиент: «Нам надо укомплектовать задвижки приводами, Тула отправила к вам», — «А какие у
            вас задвижки?» — «Двухсотые», —
            «Какого производителя?» — «Ну, мы пока не знаем, тут вот нам одна компания предлагает...» — «Ребята, ну
            зачем же вам брать у них задвижки, а у нас приводы? Возьмите у нас комплект, потому что мы ведь
            гарантированно вам состыкуем!»
            Конечно, на самом деле и другие укомпектовали бы им прекрасно, но... Тула же наш партнёр, а не их. У нас на
            складе тульские приводы всегда в наличии, причём гарантированно заводские, а не контрафактные. И просто за
            счёт этого сразу же
            растут продажи арматуры.
        </p>
    </blockquote>
