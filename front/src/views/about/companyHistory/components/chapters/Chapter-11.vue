    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</strong></p>
        <p>
            За пять лет после кризиса ЭМК превратился в крупную, устойчивую, хорошо известную на рынке компанию. Мы
            осознали себя важной частью арматурного рынка и арматурного сообщества. И вышли на порог нового этапа
            развития. Во-первых, нам
            требовалось наращивать собственный производственный потенциал, так как, оставаясь в роли торговца,
            стратегически невозможно конкурировать с крупными производителями. Во-вторых, мы осознали, что на
            достигнутом уровне мы можем уже не только
            играть по действующим на рынке правилам, но и сами влиять на эти правила. Инвестиции в структурные проекты
            не приносят быстрой отдачи, но в долгосрочном плане окупаются с лихвой.
        </p>
    </blockquote>
    <blockquote>
        <p><strong>РАССКАЗЫВАЕТ АЛЕКСЕЙ АЛЁШИН:</strong></p>
        <p>
            Примерно к 2004 г. мы как компания перешли в другое качество, на другой уровень бизнеса. Потом, конечно,
            развитие притормозило. ЭМК покупал ещё этажи в том же здании, расползался по ним, занимая, как газ, весь
            предоставленный ему объём,
            ЭМК покупал и строил заводы. Но это уже не воспринималось как новая эпоха, так как не было качественного
            скачка, не было смены мироощущения — было спокойное планомерное развитие.
        </p>
    </blockquote>
