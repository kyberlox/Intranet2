    <h5>РАССКАЗЫВАЕТ АЛЕКСАНДР ЖИЛИН:</h5>
    <blockquote>
        <p>
            Атомные контракты Пестунова свалились на КСА как раз тогда, когда на заводе начался новый виток расширения:
            взяли новые площади, стали покупать оборудование. А это же все техпроцессы надо перерисовывать, станки
            перенастраивать. И ладно бы
            арматура однотипная была, а ты поди освой почти всю атомную линейку!
        </p>
        <p>
            Конечно, режим работы был авральный. Макаров и его команда выкладывались полностью. Но нельзя ожидать от
            всех работников такого же рвения и энтузиазма, как у руководящей команды. Кто-то как сидел, так и будет
            сидеть, если его не «пинать».
            Тут нужен хороший мастер, который может продумать всё заранее, задания с вечера каждому написать, жёстко
            проконтролировать. Но найти хорошего мастера сложно, на это тоже нужно время.
        </p>
        <p>
            Таких организационных вопросов очень много, их сразу не решишь. Когда культуры производственной не хватает —
            это всегда долго. Конечно, с исполнением атомных контрактов были проблемы: брак, сроки срываются. Но мы
            старались их решить,
            разбирались, что и где не получается. Разработали положение о штрафных санкциях: люди должны понимать, за
            что их наказывают. Пришлось выгонять кого-то, кого-то штрафовать. И постепенно начало получаться. Мы
            разработали свою арматуру на
            жидкий натрий, а затем создали ещё и арматуру для свинца и для сплава «свинец-висмут», получив патент на
            изобретение. С высоким давлением намучились, конечно, но тоже сделали в конце концов...
        </p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ ДМИТРИЙ ГЛУХОВ:</h5>
    <blockquote>
        <p>
            На мой взгляд, в развитии атомной темы изначально был перекос. Вся документация на продукцию была в Москве в
            отделе Пестунова, главный конструктор был в Москве, вообще всё оказалось замкнуто на этот отдел, который
            фактически взял на себя
            функции заводских служб. Для получения быстрых коммерческих результатов такая концентрация функций
            эффективна, но на длинной дистанции она становится вредной, так как мешает заводам учиться, воспитывать
            собственные кадры. Между коммерцией
            и производством накапливалось недопонимание. Возникали ситуации, когда завод, не зная об изменениях в
            системе закупок, осваивал некую продукцию, но поставить её не мог, приходилось переделывать. А ведь любое
            конструктивное изменение
            влечет за собой многомесячное согласование по всем инстанциям, это потери времени и денег.
        </p>
        <p>
            Нам ещё повезло, что строительство Балтийской АЭС остановилось. Мы являлись основным поставщиком арматуры
            для этой стройки, и у нас были бы большие проблемы с выполнением контрактов на несколько сотен миллионов
            рублей. Когда они оказались
            не востребованы, мы за счёт полученных по ним авансов поставили другую продукцию — и таким образом сумели
            выйти из ситуации.
        </p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</h5>
    <blockquote>
        <p>
            Пестунов — он в первую очередь коммерсант, и мотивирован был так же, как другие наши коммерческие отделы —
            на рост продаж. В данном случае это оказалось не совсем правильным, ведь в атомной теме всё взаимосвязано:
            продажи, производство,
            учёт и контроль, для её развития нужно принимать и «продавливать» комплексные решения, касающиеся почти всех
            подразделений холдинга. Постепенно стало ясно, что для системного развития этого направления нужен человек,
            скажем так, с большим
            аппаратным весом, который принял бы на себя ответственность за всё в целом. Проще говоря, нужен акционер. И
            мы выдвинули из своих рядов Диму Глухова. Пару лет он вникал в тему, но смотрел на нее уже другими глазами:
            как акционер, а не как
            коммерсант. И с 2015 г. именно Дмитрий руководит этим направлением.
        </p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ ВИТАЛИЙ ПЕСТУНОВ:</h5>
    <blockquote>
        <p>
            Ту программу, на которую планировали потратить два года, мы в итоге выполнили за пять. Потому что пришлось
            перестраивать систему взглядов на заводах, много времени ушло именно на это. Но в конце концов всё
            заработало, была набрана
            необходимая инерция, было решено, что я свою задачу выполнил, Кошкарев передал тему Глухову, а я вплотную
            занялся «Техпромармой»... но это уже другая история<sup id="fnref:65"><a class="footnote-ref"
                   href="#fn:65"
                   role="doc-noteref">8</a></sup>.
        </p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ ДМИТРИЙ ГЛУХОВ:</h5>
    <blockquote>
        <p>
            Постепенно мы ситуацию с контрактами «разрулили»: что-то расторгли, что-то выполнили, заплатив штрафы за
            просрочку. И новые контракты заключаем пусть уже не в таком диком объёме (хотя всё равно в приличном), зато
            с чётким пониманием, как
            мы их будем выполнять и какую прибыль получим.
        </p>
    </blockquote>
    <h5>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</h5>
    <blockquote>
        <p>
            Так или иначе, благодаря Пестунову в отрасль мы вошли. Да, наши заводы, прежде всего КСА, оказались в
            чересчур жёстких условиях, вынуждены были учиться по принципу «бросим в воду — и пусть выплывают». Но ведь
            выплыли же в итоге! За пять
            лет с 2010 по 2014 г. ЭМК стал крупнейшим российским поставщиком атомной арматуры. Причём мы ведь всю её
            огромную номенклатуру освоили заново, с нуля!
        </p>
        <p>
            Может быть, входной билет в атомную отрасль обошёлся нам достаточно дорого, но войти в неё мы были обязаны.
            Серьёзная компания, претендующая на роль лидера на рынке арматуры, не имеет права игнорировать этот рыночный
            сегмент. Она должна
            уметь на нем работать!
        </p>
        <p>
            Нормы прибыли со времен ИФАЗа существенно упали, но если ты умеешь — на атомной теме всегда можно
            заработать. И вот теперь мы умеем, мы работаем сейчас правильно, в контакте со всеми участниками рынка, в
            нормальных деловых и человеческих
            отношениях. И я вижу в развитии атомной темы очень большую перспективу.
        </p>
