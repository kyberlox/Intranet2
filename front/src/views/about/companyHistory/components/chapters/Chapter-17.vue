            так, как в своё время со «Знаменем Труда»: продать несуществующую арматуру. Кошкарев согласился, мы
            заключили контракты на 3,5 млрд, причём благодаря моим возможностям — по вполне приемлемым ценам: вдвое выше
            себестоимости, которую я
            прекрасно себе представлял. Да, эта себестоимость — для завода с налаженным производством, но рынок же не
            будет делать тебе скидку из-за того, что оно не налажено, это твои проблемы... Зато предоплата по контрактам
            составляла до 50%, и
            всего было получено 700 млн авансов. Конечно, это была авантюра. Но куда деваться, как иначе заставить
