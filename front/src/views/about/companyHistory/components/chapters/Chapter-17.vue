            «Ты взялся, ты и разбирайся сам». Я чувствую, что тону, как в болоте... и пишу официально служебную записку
            о том, что заводы не делают то, что я говорю, потому что я не могу ни Макарову приказать, ни Алаеву, и
            ситуация получается
            неуправляемая. Поэтому я прошу назначить меня на один год «главнее всех», дать все полномочия, чтобы я мог
            решать на заводах всё сам, а не бегать по каждому вопросу доказывать свою правоту. Мне было сказано: «Нет!»
        </p>
        <p>
            И вот время идёт, а дело не сдвигается с мёртвой точки. Мои люди на зарплатах, три года деньги проедаются, а
