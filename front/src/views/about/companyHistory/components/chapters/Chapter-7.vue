<h5>РАССКАЗЫВАЕТ ИГОРЬ МЕЖЕБОВСКИЙ:</h5>
<blockquote>
    <p>
        Мы в конце концов решили открыть собственный склад в России и стали работать уже с него. Тем более ЭМК
        развил собственное производство, и наша продукция перестала играть такую роль в его продажах, как в начале
        нулевых. В результате к
        сегодняшнему дню, к сожалению, нет уже между нами такого тесного сотрудничества, как десять лет назад. Хотя
        с саратовскими ребятами мы по-прежнему дружим и с интересом следим за развитием бизнеса ЭМК.
