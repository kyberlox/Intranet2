<h5>РАССКАЗЫВАЕТ АЛЕКСАНДР СИДОРОВ:</h5>
<blockquote>
    <p>
        «Промарматуре» нужны были объёмы в России, но самим что-то организовывать — это долго и муторно, возиться с
        мелкими зарубежными фирмами, предлагая им по одному клапану, представлялось неэффективным. Они стали искать
        партнёра, который
        обеспечил бы им сбыт по России, и нашли ЭМК — тут, видимо, сыграл свою роль тот факт, что мы были знакомы по
        Украине благодаря ИФАЗу.
    </p>
    <p>
        А мы ведь работаем так: если к нам приходит какая-то заявка, неважно, что в ней написано, мы предлагаем
        только заводы свои и своих друзей. Вот как мы ИФАЗ раскрутили? Его же никто не знал, кроме «Тепловых сетей».
        А мы любую задвижку
        Пензы, Алексина, Кургана — старались заменить на ИФАЗ. Говорили: «Зачем вы берёте литую задвижку? Она же
        тяжёлая, она такая-сякая, у неё большой момент. Возьмите лучше Ивано-Франковск! Украина? Ну и что?! Мы вам
        сами всё привезём, и
        выйдет вам это дешевле».
    </p>
    <p>
        Начав работать с «Промарматурой», мы быстро стали друзьями: и Игорь Межебовский, и Саша Челядин — оба
        прекрасные люди, отвечающие за свои слова. И мы стали продвигать их продукцию так же, как и свою ИФАЗовскую.
        Уникальными были у них
        процентов тридцать позиций, а остальные семьдесят мы могли бы ещё где-то взять. Но зачем, если у нас всё уже
        на складе лежит? И мы быстро начали давать им объём продаж.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ИГОРЬ МЕЖЕБОВСКИЙ:</h5>
<blockquote>
    <p>
        На саратовской базе всегда лежало очень много арматуры с наших заводов: с Миргорода, Славгорода, Никополя. И
        Саратов имел право продавать эту арматуру сам. Причём ЭМК был нашим единственным официальным представителем
        в России. Мы несли
        все гарантийные обязательства, заводы подтверждали ценообразование. То есть склад в Саратове, с которого
        отгружались наши заказы, для Виталия был консигнационным, он мог оттуда брать, что необходимо, и продавать.
        Тем самым у ЭМК резко
        увеличились оборотные средства в виде арматуры. По сути, в России у «Промарматуры» и ЭМК были совместные
        оборотные средства — так близко мы работали.
    </p>
    <p>
        Мы прекрасно осознавали, каким мощным подспорьем для развития бизнеса ЭМК стало наше с ним сотрудничество.
        Ведь ЭМК, по сути, оказался в роли эксклюзивного представителя в России всего украинского арматуростроения.
        Но мы совершенно не
        против, чтобы наш партнёр развивался.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</h5>
<blockquote>
    <p>
        Может, стиль бизнеса у нас с ними разный, но единым был главный принцип, на котором зиждется любое
        партнёрство: «Сказал — сделал!». Они свою арматуру давали нам на полную реализацию, она лежала у нас на
        складе в огромных количествах,
        примерно на 3 миллиона долларов без особых юридических формальностей, просто на доверии. И для нас
        немыслимым было такое доверие обмануть.
    </p>
    <p>
        Мы согласовали с «Промарматурой» прайс, чтобы в нём были заложены наши 15%, я им регулярно давал
        рекомендации по увеличению или уменьшению цен (в основном, первое). Я сам придумал систему отчётности, лично
        сидел и всё проверял, не дай бог
        где-нибудь ошиблись, глупо было бы из-за какой-то мелкой ошибки подорвать доверие.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ АЛЕКСАНДР СИДОРОВ:</h5>
<blockquote>
    <p>
        В конечной цене «Промарматура» нас не ограничивала. Мы порой продавали и вдвое, и втрое выше цены расчётов с
        ними. Более того, когда мы их просили: пожалуйста, подтвердите такому-то клиенту такую-то цену, если он на
        вас выйдет напрямую —
        они давали нам такие письма. (Клиентам это нужно для отчётности: мол, они покупают в Саратове на тех же
        условиях, что и на Украине.) Они нас поддерживали всячески!
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ АЛЕКСЕЙ АЛЁШИН:</h5>
<blockquote>
    <p>
        Конечно, нержавейка сыграла тогда немалую роль в нашем развитии. Но справедливости ради нужно сказать вот
        что. Я ведь имел отношение к экономическим расчётам, к подведению ежегодных итогов работы ЭМК. В частности,
        мы оценивали стоимость
        склада: в те годы там было продукции на 200 млн руб., затем на 300 млн, потом уже сумма приближалась к
        миллиарду.
    </p>
    <p>И мне помнится, доля украинской арматуры на консигнации не была решающей — она в денежном отношении не
        превышала 10%, несмотря на то, что арматура была дорогая, там и титан лежал.</p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</h5>
<blockquote>
    <p>
        Став с начала 1999 г. эксклюзивным дилером «Промарматуры», мы продолжали ту же линию в отношении других
        украинских производителей. Основную номенклатуру промышленной арматуры мы уже имели на складе на монопольных
        условиях, но оставались
        Запорожский и Кролевецкий заводы, делавшие пароводяную арматуру из ковкого чугуна.
    </p>
    <p>
        В сентябре 1999 г. мы подписали с Запорожским арматурным заводом Протокол «О совместной деятельности по
        реализации продукции», согласно которому получали статус дилера с первоочередным рассмотрением заявок, а
        также брали на себя функции
        представительства ЗАЗ в России.
    </p>
    <p>
        А в полумёртвом Кролевце была всего одна, но интереснейшая вещь: эмалированная арматура. Завод уже почти не
        работал, и вдруг там появились инвесторы. Ребята не арматурщики, им просто завод за бесценок достался, они
        решили, что пароводяные
        клапаны — это ликвидный товар... и в принципе были правы, вот только момент выбрали неудачный: ноябрь месяц.
        Они взяли кредит, вбухали в завод миллион долларов и наделали чугунной арматуры, думая за зиму её продать.
        Но товар-то сезонный!
        В январе продаж нет, в феврале тоже, в марте — еле-еле. Начали они искать стратегических партнёров по
        реализации продукции. Обратились к нам. Чугунная арматура нас не очень интересовала, да и цена у них была
        неудачная. Но нас очень
        интересовала эмалированная арматура!
    </p>
    <p>
        Я приехал в Кролевец и спрашиваю: «Что нужно, чтобы взять у вас эксклюзив на эмаль?» Они говорят: «Выкупите
        у нас остатки». Мы выкупили, после чего они все заказы по эмали стали присылать нам. Но нам-то не эмаль
        нужна, нам клиенты нужны.
        Ведь если клиент к тебе пришёл за эмалью, он ушёл с полной корзиной арматуры и больше никуда от тебя не
        денется...
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ИГОРЬ МЕЖЕБОВСКИЙ:</h5>
<blockquote>
    <p>
        В начале нулевых сложился странный треугольник: Кошкарев с Филипповым находились друг с другом в теснейшем
        партнёрстве, но с одним мы дружили, а с другим не здоровались. При этом как у наших заводов, так и у ИФАЗа
        было немало уникальных
        позиций. Например, когда нужен сварной затвор из молибденовой стали — где его ещё взять? И мы его брали
        через Виталия Алексеевича, чтобы «ИНТЭКО» не заблокировало поставку.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ВЛАДИМИР ФИЛИППОВ:</h5>
<blockquote>
    <p>
        На мой взгляд, Кошкарев своей дружбой с «Промарматурой» нарушал всю нашу с ним схему работы в рамках
        формируемого единого бизнеса. Я тогда воспринимал это очень болезненно. Со мной он не советовался, в курсе
        не держал. Но в то время я как
        раз вплотную занялся атомкой, поэтому конфликт не раздувал.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</h5>
<blockquote>
    <p>
        Личные антипатии не должны мешать делу. В Украине мы работали вместе с Филипповым, а на российском рынке
        находились в теснейшем партнёрстве с «Промарматурой». Я не вижу в этом никаких парадоксов и противоречий.
        Для ЭМК оба партнёрства
        были очень ценными, и мы прикладывали немало усилий, чтобы их сохранить. Со временем ситуация изменилась, но
        она изменилась объективно.
    </p>
    <p>
        Тактически решение Игоря и Саши отдать нам права эксклюзивного дилера по России было верным. После кризиса
        1998 г., когда восстановились денежные расчёты, российский рынок арматуры начал быстро расти. Для
        «Промарматуры» главным
        направлением экспорта оставалась Средняя Азия, быстро освоить огромный рынок мелких российских клиентов не
        было возможности. В то же время, Славгород и Мирогород производили больше, чем могли переварить рынки
        Украины и Средней Азии вместе
        взятые. Появление хорошо знающего российский рынок дилера, конечно же, подняло продажи украинской продукции
        в России и принесло «Промарматуре» дополнительную прибыль.
    </p>
    <p>
        Однако главным капиталом торговой компании является доля рынка! Работа через дилера не позволяет
        производителю в полной мере контролировать и расширять свой рыночный сегмент. В процессе своего развития
        «Промарматура» закономерно
        задумалась о расширении своего присутствия на российском рынке за счёт комплексных поставок арматуры.
        Объективно между нами возник конфликт интересов. Изменение наших партнёрских отношений было закономерным и
        неминуемым.
    </p>
</blockquote>
<h5>РАССКАЗЫВАЕТ АЛЕКСЕЙ АЛЁШИН:</h5>
<blockquote>
    <p>
        Конечно, клиенту, пришедшему за украинской нержавейкой, мы зачастую отгружали много и других позиций, так
        что в следующий раз он наверняка обращался уже не в Миргород или в Славгород, а напрямую к нам. И, таким
        образом, новые клиенты
        появлялись у нас, а не у «Промарматуры».
    </p>
    <p>Но ведь такова и была изначальная договорённость! У них были собственные клиенты, причём очень крупные,
        которых мы принципиально обходили стороной, а на прочих они не претендовали.</p>
    <p>
        Десять лет мы таким образом работали без каких-то проблем. Но потом они решили торговать в России сами,
        создали «Союзарматуру», открыли свой склад. Мы какое-то количество у них с консигнации выкупили, остальное
        они с нашего склада вывезли
        и стали работать отдельно.
    </p>
</blockquote>
