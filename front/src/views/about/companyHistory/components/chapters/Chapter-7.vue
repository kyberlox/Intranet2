        Усть-Илимский ЛПК и ещё несколько. Мы договорились, что ЭМК туда не лезет — это их клиенты. Они мне пишут,
        например: «Заключите договор с
        Усть-Илимском на такие-то позиции на таких-то условиях». Мы заключаем договор, ведем всю техническую
        переписку, отправляем грузы. Если нужен наш товар — «Промарматура» у ЭМК его как бы покупает. То есть это её
        сделка, её контакты, её
        решения, мы просто исполнители, наше — только юрлицо.
    </p>
    <p>
        Но собственных клиентов у «Промарматуры» было настолько мало, что нас это совершенно не напрягало. Объёмы
        наших продаж как российского дилера «Промарматуры» были гораздо выше. Ведь арматура из нержавейки — это
        серьёзный рынок: химия,
        нефтехимия, минеральные удобрения. А в России многих позиций такой арматуры не производилось. Клиенты
        обращаются на Украину, но сами с Украиной работать не хотят. Мы договорились действовать по той же схеме,
        что и с ИФАЗом: заявки из
        России на заводах «Промарматуры» принимаются только в письменной форме, они их нам пересылают, и мы с
        клиентами работаем, заодно предлагая им и другие позиции с нашего склада.
    </p>
    <p>
        Кроме прочего, мы за весьма условную плату помогали «Промарматуре» с поставками российского оборудования в
        Узбекистан, в адрес Навоийского ГМК, их основного клиента. Через некоторое время по просьбе «Промарматуры»
        мы стали оказывать
        подобные услуги ещё и фирме «КраМан», созданной Сергеем Красковским и Натальей Мандрыкиной после того, как
        они перебрались из Узбекистана в Москву. Это знакомство оказалось в будущем очень ценным для нас!
    </p>
</blockquote>
