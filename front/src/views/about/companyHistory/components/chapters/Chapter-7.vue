<h1 class="page__title">Глава 7. Украинская арматура</h1>
<p>
    В Советском Союзе внешняя торговля была монополией государства, занимались ею Всесоюзные Внешнеторговые
    объединения — специальные организации в структуре Минвнешторга. Сами же заводы, закупающие импортное
    оборудование, не были знакомы ни с
    валютными операциями, ни с таможней. К тому же экономика СССР была в существенной степени самодостаточной,
    основная доля оборудования производилась внутри страны. Так что для большинства промышленных предприятий импорт
    был чем-то далёким,
    чуждым, сложным и непонятным. Но после распада Союза между бывшими республиками возникли границы, во всех новых
    государствах были введены собственные валюты. А ведь многие номенклатурные позиции промышленного оборудования
    производились в СССР
    на единственном заводе. И если, допустим, этот завод расположен в Украине, то для российских потребителей его
    продукция теперь стала импортной, а заменить её нечем: почти всё российское машиностроение в 1990-е гг. было
    озабочено банальным
    выживанием, ему было не до освоения новой продукции.
</p>
<p>
    Итак, потребность в импортных поставках после распада СССР существенно возросла, но заниматься этим никто толком
    не умел. Поэтому клиенты готовы были покупать арматуру у дилера со склада в России дороже, лишь бы не
    связываться с валютными и
    таможенными операциями. Речь идёт прежде всего о продажах в России продукции украинских заводов. Ведь именно в
    Украине производились уникальные номенклатурные позиции, не имевшие российских аналогов, к тому же себестоимость
    украинской
    арматуры была существенно ниже.
</p>
<p>
    ЭМК работал с Украиной с самого своего основания, прекрасно знал всех украинских производителей. Благодаря
    покупке ИФАЗа он к тому же стал «своим» в украинском арматуростроении. И выработал рабочую стратегию, которая к
    началу нулевых привела
    к тому, что на российском рынке общепромышленной арматуры ЭМК фактически стал представителем целой страны.
</p>
