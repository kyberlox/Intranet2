</blockquote>
<h5>РАССКАЗЫВАЕТ ВИТАЛИЙ КОШКАРЕВ:</h5>
<blockquote>
    <p>Честно говоря, я думал, что они откроют собственный склад гораздо раньше — и я бы их прекрасно понял. Бизнес
        есть бизнес, а уж после той большой роли, которую «Промарматура» сыграла в развитии ЭМК, обижаться было бы
        просто грешно.</p>
    <p>
        Фактически в начале нулевых ЭМК стал представителем на российском рынке в России всех действующих украинских
        арматурных заводов, нередко обладая эксклюзивом на их продукцию. Российские клиенты этих заводов стали
        нашими, благодаря чему ЭМК
        рос очень серьёзными темпами, постепенно превращаясь в игрока высшей лиги на рынке арматуры. Правильно
        выбранная стратегия принесла свои плоды.
    </p>
</blockquote>
