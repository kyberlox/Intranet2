            «...!! Где твои засувки?!
            У меня завтра станция взорвется!!» А я-то был уверен, что арматура — это такие стальные прутки, как из-за
            них что-то может взорваться? Я позвонил Боре, он в двух словах объяснил, что такое «засувки», связал меня с
            Филипповым. Выяснилось,
            что была зачётная сделка, посреднику станция заплатила всю сумму, но на завод пришло только 10%, после чего
            исчез и посредник, и остальные деньги. А задвижки для станции готовы, лежат, ждут оплаты. Я говорю:
            «Владимир Витольдович, ваши
            задвижки станции очень нужны! Если их отдать — вы сильные убытки понесете?» — «Да нет, в принципе. Отдадим,
            если надо». И я договорился с Карраском, что ИФАЗ грузит ему задвижки, приняв убытки на себя, но он в ответ
