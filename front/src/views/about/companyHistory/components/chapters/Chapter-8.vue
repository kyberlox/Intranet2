            что больше у меня нет никаких рычагов контроля ИФАЗа. Андрющенко полноценной заменой Тоскарю в этом смысле
            не стал. У нас вправду
            был с Димой непростой разговор. Он делал на Украине много полезного для нас, расставаться с таким сильным и
            опытным менеджером всегда тяжело. Но главное — с его отъездом мы, считай, ИФАЗ потеряли. Я понимал, что рано
            или поздно, если ты
            не контролируешь актив, его у тебя не будет. Так в конце концов и случилось. Если бы в 2008 г. на ИФАЗе был
            Тоскарь — я бы знал, что всё имущество завода оказалось в залоге под кредиты, взятые «ИНТЭКО». Но тогда я
            ничего сделать не смог,
            конфликт интересов был объективным, и Дима уехал. Наше партнёрство с Филипповым в тот момент выглядело
            прочным и прозрачным, в нём у каждого была своя тема, а заработавший на полную мощность ИФАЗ являлся
            связующим звеном. Но управлял им
            Филиппов, а ЭМК оказался по отношению к ИФАЗу практически в той же роли, что и к заводам «Промарматуры». Ни
            возможности, ни мотивации конкретно заниматься развитием завода у ЭМК не было.
        </p>
