            «бандит» Филиппов... Непонятно, кто их писал, кому это надо было. Филиппов говорил, мол, какие-то
            конкуренты. В общем, «ИНТЭКО» было представлено как бандитская структура, которая захватила ИФАЗ. Поэтому
            АСЭ сварные задвижки собирался
