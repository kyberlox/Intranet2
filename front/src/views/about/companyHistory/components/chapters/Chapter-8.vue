            страховую компанию, обслуживающую атомные станции. Никто из нас толком не понимал, что такое страхование, но
            времена были такие, что
            достаточно было иметь каплю здоровой наглости... В общем, через год компания была создана. Мы поехали по
            российским АЭС, убеждая их, что «во всем мире есть, а у нас нет, нам тоже нужно!» Ну и убедили. Правда,
            вышло немного иначе, чем
            хотелось: станции создали свою собственную страховую компанию с участием Концерна «Росэнергоатом», а мы
            втроём оказались в ней наёмными менеджерами. Да, акций у нас в итоге не было, зато мы получили допуск в
            элиту атомной отрасли, и за
            несколько лет я хорошо изучил все атомные станции: знал всех директоров и всех замов.
        </p>
        <p>
            Дела пошли хорошо, и, как это часто бывает в таких случаях, среди трёх друзей-управляющих возникли
            разногласия. Один из нас был директором, а двое работали «в поле». Директор решил, что мы ему не особо
            нужны. И в 1997 г. мы решили
            разойтись. После чего компания проработала ещё пару лет... Обычная ситуация: начальнику кажется, что он —
            самая важная фигура и справится сам, но после того как уходят «боевые единицы» — фирма долго не живёт.
        </p>
