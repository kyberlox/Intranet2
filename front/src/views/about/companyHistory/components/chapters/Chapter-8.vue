            там... и решил вместо банковской деятельности заняться атомной арматурой. 10 января 1998 г. я стал
            сотрудником «Армторга», и мы
            начали осваивать атомный рынок России. Станции готовы были брать арматуру ИФАЗа. Но нужно было разрешение
            проектанта. Я поехал по АЭПам: так, мол, и так, есть такой ИФАЗ. «Так вы живы?! А дайте нам хоть
            что-нибудь!» — «Что?» — «Хотя бы
            каталог, а лучше ТУ». Я звоню на завод: «Вышлите мне все ТУ на атомную арматуру», — «Нельзя! Коммерческая
            тайна!» Прочитал я ТУ — какая там тайна? Это же не КД<sup id="fnref:27"><a class="footnote-ref"
                   href="#fn:27"
                   role="doc-noteref">2</a></sup>! Позвонил Филиппову: «Володя, нужны ТУ! Ну нет там ничего секретного!»
