            арматурного завода. Я поехал, с проблемой разобрался, меня поблагодарили и говорят: «Слушайте, у Вас такие
            связи! Вы не хотите с нами
            поработать?» Речь шла о фирме «Армторг», которую возглавлял Боря Самойлов, а завод был как раз
            ИваноФранковский. Меня в тот момент звали в «Альфа-банк», но там процедура приёма на работу занимала три
            месяца. И я подумал: ну, поезжу пока,
            помогу ребятам. Не бесплатно же.
        </p>
