            Филиппову и Кошкареву, но внятного ответа не получил. Кошкарева украинский рынок не интересовал, а «ИНТЭКО»,
            видимо, предпочитало потерять стратегического клиента, лишь бы не дать мне заработать. Я понял, что никакой
            перспективы у меня на
            ИФАЗе нет. Не то что командная работа, даже обычное рабочее взаимодействие полностью отсутствовало. Более
            того, появилось ещё и неприятное ощущение небезопасности. И я ушёл. Кошкарев был очень недоволен моим
