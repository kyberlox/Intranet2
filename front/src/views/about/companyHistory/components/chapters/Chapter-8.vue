            АЭП прислал письмо на ИФАЗ: «Предлагаем принять участие в поставке продукции по заказу «Атомстройэкспорта»;
            подтвердите, что вы можете сделать такую-то арматуру». ИФАЗ ответил: «Нет, наш завод не может этого
            сделать». А мы в «ИНТЭКО» даже
            не знали, что был такой запрос.
        </p>
    </blockquote>
